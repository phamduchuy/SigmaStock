    @Override
    protected void onResume() {
        super.onResume();

        // register GCM registration complete receiver
        LocalBroadcastManager.getInstance(this).registerReceiver(mRegistrationBroadcastReceiver,
                new IntentFilter(Config.REGISTRATION_COMPLETE));

        // register new push message receiver
        // by doing this, the activity will be notified each time a new message arrives
        LocalBroadcastManager.getInstance(this).registerReceiver(mRegistrationBroadcastReceiver,
                new IntentFilter(Config.PUSH_NOTIFICATION));

        // clear the notification area when the app is opened
        NotificationUtils.clearNotifications(getApplicationContext());
    }

    @Override
    protected void onPause() {
        LocalBroadcastManager.getInstance(this).unregisterReceiver(mRegistrationBroadcastReceiver);
        super.onPause();
    }

    private void setupViewPager(ViewPager viewPager)
    {
        ViewPagerAdapter adapter = new ViewPagerAdapter(getSupportFragmentManager());
        fragmentList =  new ArrayList<>();

        String[] arrayName  = {"Trang chủ","Lịch cổ tức","Danh mục","Khuyến Nghị"};
        for (int i=0;i< 4;i++)
        {
            CFragment cFragment = new CFragment();
            cFragment.setName(arrayName[i]);
            cFragment.setUrl("");
            fragmentList.add(cFragment);
        }

        viewPager.setAdapter(adapter);


    }


    class ViewPagerAdapter extends FragmentPagerAdapter {
        private final List<android.support.v4.app.Fragment> mFragmentList = new ArrayList<>();

        public ViewPagerAdapter(FragmentManager manager) {
            super(manager);
        }

        @Override
        public android.support.v4.app.Fragment getItem(int position) {
            switch (position)
            {
                case 0:
                    return  FmHome.newInstance(fragmentList.get(position).getName(),fragmentList.get(position).getUrl());
                case 1:
                    return  FmDividend.newInstance(fragmentList.get(position).getName(),fragmentList.get(position).getUrl());
                case 2:
                    return  FmCategory.newInstance(fragmentList.get(position).getName(),fragmentList.get(position).getUrl());
                case 3:
                    return  FmRecommendations.newInstance(fragmentList.get(position).getName(),fragmentList.get(position).getUrl());



            }
            return mFragmentList.get(position);
        }



        @Override
        public int getCount() {
            return fragmentList.size();
        }

        @Override
        public CharSequence getPageTitle(int position) {
            return fragmentList.get(position).getName();
        }
    }

